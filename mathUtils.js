

// Addition Functions:

export function getSum(numOne, numTwo) {
    return numOne + numTwo;
}

// Subtraction Functions:

export function getDiff(numOne, numTwo) {
    return numOne - numTwo;
}

// Multiplication Functions:

export function getProd(numOne, numTwo) {
    return numOne * numTwo;
}

// Division Functions:

export function getSolution(numOne, numTwo) {
    return numOne / numTwo;
}